package com.dgut.bluetoothdemo;

public class ChatMessage {
    public int type;
    public String content;

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }
}
